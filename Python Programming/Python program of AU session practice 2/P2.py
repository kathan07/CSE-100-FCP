t = int(input("Enter time as per 24 hrs clockwise\n"))
if 0<t<4:
    print("Good night")
elif 4<t<11:
    print("Good Morning")
elif 11<t<13:
    print("Good noon")
elif 13<t<16:
    print("Good afternoon")
elif 16<t<18:
    print("Good evening")
elif 18<t<24:
    print("Good night")


